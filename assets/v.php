            <h2 class="section-title" style="font-size: 35px;">ascent</h2>
            <p> 
              ASCENT is a highly extensible e-governance assessment tool for Uganda's new cities of Jinja, FortPortal, Masaka, Mbale, Arua, Gulu, Hoima, Lira, Soroti, Entebbe, Moroto, Nakasongola, Kabale, Wakiso, Mbarara. The  tool guides better understanding of the overall smart city development and governance for Uganda’s new cities; performing comparisons on the levels of e-governance and establishing benchmarks among the new cities.To compare e-governance levels, ASCENT is able to assess the levels of e-readiness across the cities that include information security, information resources, cloud environments, innovation characteristics, application performances, and the e-readiness index among others. ASCENT will be achievable through leveraging ICT to support the governance process and enhance service delivery for various sectors in the new cities. The project developed a formative e-governance assessment tool that should be used to continuously assess the new cities for concerted efforts towards realising smart cities and support the adoption of ICTs for effective administration and improved service delivery in the new cities.The solution aligns with Uganda’s Vision 2040 and NDP III (2020/2021 - 2024/2025) frameworks that envisage transforming society to middle-income status using ICTs to leverage all sectors of the economy.
              The ASCENT Framework is composed of the following parameters: Physical Planning, Built Environment & Housing, Energy (Electricity & Gas), Telecommunications, Mobility/Transportation, Health and Human Services, Water and Waste Water, Waste Management, Payments  & Finance, Education & Skills, Public Safety & Security, Inclusiveness, Equity & Good Governance, Institution, Structuring & Integration, Access to Services, Jobs & Income

